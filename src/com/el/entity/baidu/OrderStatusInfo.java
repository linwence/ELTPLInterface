package com.el.entity.baidu;

public class OrderStatusInfo {
	private String order_id;// 百度订单编号
	private int status;// 参见附录：订单状态对照表

	public OrderStatusInfo() {

	}

	public OrderStatusInfo(String order_id, int status) {
		super();
		this.order_id = order_id;
		this.status = status;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}
}
